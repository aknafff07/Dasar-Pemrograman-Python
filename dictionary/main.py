# list -> array, mengakses data menggunakan index
data_list = ['ucup','aknaf']
print(data_list[1])

# dictionary (dict) -> associative array
# indentifier -> key
data_dict = {
    'key': 'value',
    'cp': 'ucup',
    'anp': 'aknaf',
    'ddg': 'dudung',
    'number': 100,
    'list': data_list
}
print(data_dict)
print(data_dict['cp'])
print(data_dict['number'])
print(data_dict['list'])