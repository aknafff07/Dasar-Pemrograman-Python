# perulangan (loop)


# for kondisi:
#   aksi

# ini dengan list
angka1 = [0,2,4,8,10]
print(angka1)

for i in angka1:
    print(f"i sekarang -> {i}")
    
print('akhir program')

# ini dengan range
for i in range(5):
    print(f"i sekarang -> {i}")
    
print('akhir program')

for i in range(1,7):
    print(f"i sekarang -> {i}")
    
print('akhir program')

# menggunakan string
data_str = "saya ganteng"

for huruf in data_str:
    print(huruf)
    
print("akhir program")